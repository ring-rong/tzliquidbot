def retry_message(attempt: int) -> str:
    index = min(attempt - 1, len(_RETRY_TEXTS) - 1)
    return _RETRY_TEXTS[index]


def summary_card(name: str, phone: str, preferred_time: PreferredTime) -> str:
    return (
        "Проверьте, пожалуйста, Ваши данные:\n\n"
        f"Имя: {name}\n"
        f"Телефон: {phone}\n"
        f"Удобное время: {time_label(preferred_time)}\n\n"
        "Всё верно?"
    )


def success_text(name: str) -> str:
    return f"Спасибо, {name}! Заявка отправлена, с Вами свяжутся в выбранное время."
